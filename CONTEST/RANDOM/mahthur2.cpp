using ll = long long int;
const ll MAXN = 2000005;
const ll SFT = 1000005;
ll bit[MAXN];

ll sum(ll r) {
    ll ret = 0;
    for (; r >= 0; r = (r & (r + 1)) - 1)
        ret += bit[r];
    return ret;
}


void add(ll idx, ll delta) {
    for (; idx < MAXN; idx = idx | (idx + 1))
        bit[idx] += delta;
}

void solve()
{
	string s;
	cin>>s;
	memset(bit, 0, sizeof(bit));
	ll n= s.length();
	add(0+SFT, 1);
	ll cnt1=0,cnt0=0;
	ll ans=0;
	for(int i=0;i<n;i++)
	{
		if(s[i]=='1')
		{
			cnt1++;
		}
		else{
			cnt0++;
		}
		ll x = cnt1-cnt0;
		ll q = sum(x-1+SFT);
		ans+=q;
		add(x+SFT,1);
	}
	cout<<ans<<endl;
	return;
}


